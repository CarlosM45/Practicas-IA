# Exponenciación
print(2**3)

# Multiplicación
print(2*3)

# División
print(6/3)

# División entera, que devuelve un número entero ignorando los decimales necesarios para el resultado exacto
print(6//4)

# Residuo, que realiza la división entera pero devuelve el residuo como resultado
print(14 % 4)

# Suma
print(-4+4)

# Resta
print(-4-4)
