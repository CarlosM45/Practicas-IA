#Operador de igualdad, pregunta "¿es a igual a b?"
var=0
print(var==0)#cierto
var=1
print(var==0)#falso

#Operador de desigualdad, pregunta "¿a no es igual a b?"
var=0
print(var!=0)#falso
var=1
print(var!=0)#cierto

#Operadores de comparación
print(var>0)#mayor que (en este caso cierto)
print(var>=0)#mayor o igual que (también cierto)
print(var<=0)#menor o igual que (falso)