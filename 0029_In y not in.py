# Los operadores in y not in comparan un valor con una lista, para devolver si se encuentra o no el elemento, respectivamente
colores_favoritos = ["Rojo", "Verde", "Azul"]
# Experimentamos con lo dicho anteriormente
print("Rojo" in colores_favoritos)
print("Morado" in colores_favoritos)
print("Azul" not in colores_favoritos)
